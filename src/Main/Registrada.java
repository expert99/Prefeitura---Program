    public void data(){
         
        try {
            conexao = c.getConexao();
            String pegarid = "select id from cartas order by id desc";
            pst = conexao.prepareStatement(pegarid);
            
            String sql = "insert into cartas (remetente, destinatario, carta, codigo, data, CEP, situação) values ('"+this.txtde.getText()+"','"+this.txtpara.getText()+"', 'Registrada', '"+this.txtcodigo.getText()+"', '"+this.txtdata.getText()+"', '"+this.txtcep.getText()+"', 'Enviado')";
            pst = conexao.prepareStatement(sql);
            
            if(this.txtpara.getText().isEmpty() || this.txtde.getText().isEmpty() || this.txtcodigo.getText().isEmpty() || this.txtdata.getText().isEmpty() || this.txtcep.getText().isEmpty()){
                
            JOptionPane.showMessageDialog(null, "Preencher Todos os Campos");
                
                
            } else{
                
                pst.executeUpdate();
                rs = pst.executeQuery(pegarid);
                if(rs.next()){
                pegarid = rs.getString("id");
                JOptionPane.showMessageDialog(null, "Inclusão Realizada com sucesso, sua id é: "+pegarid);
                conexao.close();
                txtpara.setText(null);
                txtde.setText(null);
                txtcodigo.setText(null);
                txtdata.setText(null);
                txtcep.setText(null);
                
                
                
                 }
                
                
                
            }
            
            
            
            
            
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        
        
        
    }
    
    

    
